#include <stdio.h>
/**
*op_mod - calculate the modulus
*@a: first parameter
*@b: second parameter
*
*Return: result
*/
int op_mod(int a, int b)
{
	return (a % b);
}
/**
*op_mul - multiplies
*@a: first parameter
*@b: second parameter
*
*Return: result
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
*op_add - addition of numbers
*@a: first parameter
*@b: second parameter
*
*Return: result
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
*op_div - divides numbers
*@a: first parameter
*@b: second parameter
*
*Return: result
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
*op_sub - suntracts numbers
*@a: first parameter
*@b: second parameter
*
*Return: result
*/
int op_sub(int a, int b)
{
	return (a - b);
}
